
public class EvenNumberDivisibleByThree {
	public static void EvenNumber(int n)
	{
		if(n%2==0)
		{
			if(n%3==0)
		  	{
				System.out.println("Entered Number "+n+" is even number divided by 3");
				
			}
			else 
			{
				System.out.println("Entered Number "+n+" is even number divided by 3");
			}
		}
	}
}
